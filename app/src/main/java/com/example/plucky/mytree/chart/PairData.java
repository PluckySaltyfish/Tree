package com.example.plucky.mytree.chart;


public class PairData {
    private int ValueX,ValueY;

    public PairData(int valueX, int valueY) {
        ValueX = valueX;
        ValueY = valueY;
    }

    int getValueX() {
        return ValueX;
    }

    int getValueY() {
        return ValueY;
    }
}
